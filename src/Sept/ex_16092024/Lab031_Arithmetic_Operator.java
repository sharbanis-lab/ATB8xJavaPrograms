package Sept.ex_16092024;

public class Lab031_Arithmetic_Operator {
    public static void main(String[] args) {
        //Arithmetc Operator
        //+, - ,* , / -> These are Arithmetc Operators
        //They are Binary in Nature -> Needs 2 Operands

        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);

        //% Modulus is also Arithematic Operator that gives Remainder
        System.out.println(a%b);
    }
}
